#include<stdio.h>
int main()
{
    int a=876;
    short b=5215;
    long int c=576543L;
    unsigned int d=9578U;
    printf("integer data type %d\n",a);
    printf("short integer data type %d\n",b);
    printf("long integer data type %ld\n",c);
    printf("unsigned integer data type %u",d);
    return 0;
}